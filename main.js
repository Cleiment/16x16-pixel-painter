const drawArt = (arr = artArray) => {
  pixelCanvas.innerHTML = null;
  for (let i = 1; i <= arr.length; i++) {
    const el = document.createElement('div');
    arr[i] ? el.classList.add('art-pixel', arr[i]) : el.classList.add('art-pixel');
    el.setAttribute('position', i);
    pixelCanvas.appendChild(el);
    el.addEventListener('click', e => {
      if (arr[i]) {
        addColor(e.target.getAttribute('position'), null);
      } else {
        addColor(e.target.getAttribute('position'));
      }
      drawArt();
    })
  }
}

const addColor = (position, color = 'skin') => {
  artArray[position] = color;
}

drawArt();

document.getElementById('btn-rabbit-helmet').addEventListener('click', (e) => drawArt(rabbitHelmet));
document.getElementById('btn-reset').addEventListener('click', (e) => drawArt());